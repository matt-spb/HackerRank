func hourglassSum(arr: [[Int]]) -> Int {
    var arrayOfSums = [Int]()
    for i in 0..<4 {
        for j in 0..<4 {
            let sum = arr[i][j] + arr[i][j+1] + arr[i][j+2] + arr[i+1][j+1] + arr[i+2][j] + arr[i+2][j+1] + arr[i+2][j+2]
            arrayOfSums.append(sum)
        }
    }
    return arrayOfSums.max() ?? 0
}

